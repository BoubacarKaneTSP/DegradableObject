package eu.cloudbutton.dobj.utils;

import org.jetbrains.annotations.NotNull;

import java.util.*;

public class ImmutableComposedSet<K> implements Set<K> {

    private Set<K>[] sets;

    public ImmutableComposedSet(Set<K>[] sets) {
        this.sets = sets;
    }

    @Override
    public int size() {
        return 0;
    }

    @Override
    public boolean isEmpty() {
        return false;
    }

    @Override
    public boolean contains(Object o) {
        return false;
    }

    @NotNull
    @Override
    public Iterator<K> iterator() {
        Collection<Iterator<K>> collection = new ArrayList<>(sets.length);
        Arrays.stream(sets).map(s -> collection.add(s.iterator()));
        return new ComposedIterator<>(collection);
    }

    @NotNull
    @Override
    public Object[] toArray() {
        return new Object[0];
    }

    @NotNull
    @Override
    public <T> T[] toArray(@NotNull T[] a) {
        return null;
    }

    @Override
    public boolean add(K k) {
        return false;
    }

    @Override
    public boolean remove(Object o) {
        return false;
    }

    @Override
    public boolean containsAll(@NotNull Collection<?> c) {
        return false;
    }

    @Override
    public boolean addAll(@NotNull Collection<? extends K> c) {
        return false;
    }

    @Override
    public boolean retainAll(@NotNull Collection<?> c) {
        return false;
    }

    @Override
    public boolean removeAll(@NotNull Collection<?> c) {
        return false;
    }

    @Override
    public void clear() {

    }

    @Override
    public boolean equals(Object o) {
        return false;
    }

    @Override
    public int hashCode() {
        return 0;
    }
}
